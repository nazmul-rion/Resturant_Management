    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed

        calbtn.doClick();

        try {
            String sql = "INSERT INTO selected_item_table(ID, Name, Rate, Discount, Qty, Ammount) VALUES(?,?,?,?,?,?)";
            pst = database.con.prepareStatement(sql);
            pst.setString(1, idtxt.getText());
            pst.setString(2, item_nametxt.getText());
            pst.setString(3, ratetxt.getText());
            pst.setString(4, distxt.getText());
            pst.setString(5, qtytxt.getText());
            pst.setString(6, totaltxt.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Adding Successfull");
            showselectedtablelist();
            totalcalculation();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }


    }//GEN-LAST:event_addbtnActionPerformed

    private void calbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calbtnActionPerformed

        double total, rate, discount, qty;
        String Total;
        rate = Double.parseDouble(ratetxt.getText());
        discount = Double.parseDouble(distxt.getText());
        qty = Double.parseDouble(qtytxt.getText());
        total = (((rate - (rate * discount) / 100)) * qty);

        Total = Double.toString(total);
        totaltxt.setText(Total);


    }//GEN-LAST:event_calbtnActionPerformed

