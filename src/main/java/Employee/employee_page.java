    public void showtablelist() {
        try {
            String sql = "SELECT ID, Name, Rate, Discount FROM product_list";
            pst = database.con.prepareStatement(sql);
            rs = pst.executeQuery();
            table_list.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

    public void showselectedtablelist() {
        try {
            String sql = "SELECT * FROM selected_item_table";
            pst = database.con.prepareStatement(sql);
            rs = pst.executeQuery();
            selected_table.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }

    public void showselectedtabledelete() {
        try {
            String sql = "DELETE FROM selected_item_table";
            pst = database.con.prepareStatement(sql);
            pst.executeUpdate();
            showselectedtablelist();
            stotaltxt.setText("");
            fullcosttxt.setText("");
            fulldistxt.setText("");
            vattxt.setText("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }

    public void totalcalculation() {

        {
            double sum = 0;
            for (int i = 0; i < selected_table.getRowCount(); i++) {
                sum = sum + Double.parseDouble(selected_table.getValueAt(i, 5).toString());
            }

            stotaltxt.setText(Double.toString(sum));
        }

    }

